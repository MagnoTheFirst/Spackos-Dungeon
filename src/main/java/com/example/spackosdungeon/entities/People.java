package com.example.spackosdungeon.entities;

public class People {
    final String NAME;
    final String DESCRIPTION;
    final Boolean GENDER;
    final String GREETING_TXT;
    final String ANSWER;

    public People(String NAME, String DESCRIPTION, Boolean GENDER, String GREETING_TXT, String ANSWER) {
        this.NAME = NAME;
        this.DESCRIPTION = DESCRIPTION;
        this.GENDER = GENDER;
        this.GREETING_TXT = GREETING_TXT;
        this.ANSWER = ANSWER;
    }



}
